package Basics.VariablesAndDataTypes;

public class Variables {

    public static void main(String[] args) {
        //Declaring variables.
        //Variable is a placeholder in memory associated with information.

        //To hold an integer.
        //Printing the integer.
        int value = 10;
        System.out.println("The value of the integer variable is: " +value);

        //To hold a String.
        String text = "This is a string";
        System.out.println("This is a String: " +text);
    }
}
